var drawLogo = function(ctx, obj) {
	ctx.save();
		ctx.translate(obj.x,obj.y);
		ctx.scale(obj.scale,obj.scale);
		ctx.shadowColor = "Black";
		ctx.shadowBlur = 15; 
		ctx.globalAlpha = obj.alpha;
		ctx.drawImage(obj.pic, -41, -41);
	ctx.restore();
};